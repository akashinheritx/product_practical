const { body } = require('express-validator');

//validate global booster settings form detail
exports.globalBoosterSettingsValidator = [
    body('boosterName')
        .not()
        .isEmpty()
        .withMessage('GLOBAL_BOOSTER.BOOSTER_NAME')
        .trim()
        .isString()
        .withMessage('GLOBAL_BOOSTER.BOOSTER_NAME_STRING'),
    body('boosterType')
        .not()
        .isEmpty()
        .withMessage('GLOBAL_BOOSTER.BOOSTER_TYPE')
        .trim()
        .isNumeric()
        .withMessage('GLOBAL_BOOSTER.BOOSTER_TYPE_NUMERIC'),
    body('boosterCount')
        .not()
        .isEmpty()
        .withMessage('GLOBAL_BOOSTER.BOOSTER_COUNT')
        .trim()
        .isNumeric()
        .withMessage('GLOBAL_BOOSTER.BOOSTER_COUNT_NUMERIC'),
    body('boosterPrice')
        .not()
        .isEmpty()
        .withMessage('GLOBAL_BOOSTER.BOOSTER_PRICE')
        .trim()
        .isNumeric()
        .withMessage('GLOBAL_BOOSTER.BOOSTER_PRICE_NUMERIC'),
    body('boosterPoint')
        .not()
        .isEmpty()
        .withMessage('GLOBAL_BOOSTER.BOOSTER_POINT')
        .trim()
        .isNumeric()
        .withMessage('GLOBAL_BOOSTER.BOOSTER_POINT_NUMERIC'),
    body('requiredIds')
        .not()
        .isEmpty()
        .withMessage('GLOBAL_BOOSTER.IDS_COUNT')
        .trim()
        .isNumeric()
        .withMessage('GLOBAL_BOOSTER.IDS_COUNT_NUMERIC'),
    body('boosetrDetail')
        .not()
        .isEmpty()
        .withMessage('GLOBAL_BOOSTER.BOOSTER_DETAILS')
        .trim()
        .isString()
        .withMessage('GLOBAL_BOOSTER.BOOSTER_DETAILS_STRING'),

];

